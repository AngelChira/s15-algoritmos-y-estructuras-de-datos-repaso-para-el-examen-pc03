    public void mostrar() {
        if (pilaProveedor.size() > 0) {
            taResult.setText("CODIGO\t APELLIDOS\t NOMBRES");
            for(Proveedor pro: pilaProveedor.lista()) {
                if (pro!= null) {
                    taResult.append("\n"+pro.toString());
                }
            }
        } else {
            taResult.setText("No hay proveedores.");
        }
    }

    public boolean verificarCod(Proveedor prove) {
        if (pilaProveedor.size() > 0) {
           for(Proveedor pro: pilaProveedor.lista()) {
                if (pro != null && pro.getCod().equalsIgnoreCase(prove.getCod())) {
                    return false;
                }
            }
        }
        return true;
    }
    
    public void buscarProveedor(Proveedor provee) {
        count =0;
        if (pilaProveedor.size() > 0) {
            for(Proveedor pro: pilaProveedor.lista()) {
                if (pro != null && pro.getCod().equalsIgnoreCase(provee.getCod())) {
                    System.out.println("---");
                    taResult.setText("PERSONA ENCONTRADA\nCODIGO\t APELLIDOS\t NOMBRES");
                    taResult.append("\n"+pro.toString());
                    count++;
                }
            }
        }
    }

