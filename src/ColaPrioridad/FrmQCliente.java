    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        int codigo = Integer.parseInt(txtCod.getText().strip());
        if (verificarCod(new QCliente(codigo))) {
            byte edad = Byte.parseByte(txtApell.getText().strip());
            String tipo = cboTipo.getSelectedItem().toString();
            QCliente cliente = null;
            if (tipo.equalsIgnoreCase("GOLD")) {
                cliente = new QCliente(codigo, txtNom.getText().strip(), edad, QCliente.TipoCliente.GOLD,obtenerAñoMesDiaHoraMinuto());
            } else if (tipo.equalsIgnoreCase("REGULAR")) {
                cliente = new QCliente(codigo, txtNom.getText().strip(), edad, QCliente.TipoCliente.REGULAR,obtenerAñoMesDiaHoraMinuto());
            } else if (tipo.equalsIgnoreCase("PREMIUN")) {
                cliente = new QCliente(codigo, txtNom.getText().strip(), edad, QCliente.TipoCliente.PREMIUN,obtenerAñoMesDiaHoraMinuto());
            } else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo");
                return;
            }            
            qcolaCliente.enqueue(cliente);
            txtCod.setText("");
            txtNom.setText("");
            txtApell.setText("");
            cboTipo.setSelectedIndex(0);
            mostrar();
        } else {
            JOptionPane.showMessageDialog(null, "El codigo ya existe.");
            txtCod.requestFocus();
            taResult.setText("No se agrego el user.");
        }
    }//GEN-LAST:event_btnInsertActionPerformed

