    public void mostrar() {
        if (qcolaCliente.size() > 0) {
            taResult.setText("CODIGO\t APELLIDOS\t NOMBRES");
            for(QCliente user: qcolaCliente.listar()) {
                if (user!= null) {
                    taResult.append("\n"+user.toString());
                }
            }
        } else {
            taResult.setText("No hay usuarios.");
        }
    }

    public boolean verificarCod(QCliente user) {
        if (qcolaCliente.size() > 0) {
           for(QCliente us: qcolaCliente.listar()) {
                if (us != null && us.getOrden() == user.getOrden()) {
                    return false;
                }
            }
        }
        return true;
    }
    
    public LocalDateTime obtenerAñoMesDiaHoraMinuto() {
        Random random = new Random();

        int year = random.nextInt(2023 - 2000 + 1) + 2000; // Año entre 2000 y 2023
        int month = random.nextInt(12) + 1; // Mes entre 1 y 12
        int day = random.nextInt(28) + 1; // Día entre 1 y 28 (para simplificar)
        int hour = random.nextInt(24); // Hora entre 0 y 23
        int minute = random.nextInt(60); // Minuto entre 0 y 59

        return LocalDateTime.of(year, month, day, hour, minute);
    }
    
    public int orden() {
        return orden++;
    }
    
    public void buscarProveedor(QCliente user) {
        count =0;
        if (qcolaCliente.size() > 0) {
            for(QCliente us: qcolaCliente.listar()) {
                if (us != null && us.getOrden() == user.getOrden()) {
                    taResult.setText("USER ENCONTRADO\nCODIGO\t APELLIDOS\t NOMBRES");
                    taResult.append("\n"+us.toString());
                    count++;
                }
            }
        }
    }

