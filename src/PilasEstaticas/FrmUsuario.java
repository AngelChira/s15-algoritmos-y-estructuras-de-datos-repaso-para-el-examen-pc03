    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String codigo = txtCod.getText().strip();
        if (verificarCod(new Proveedor(codigo))) {
            pilaProveedor.push(new Proveedor(txtCod.getText().strip(), txtNom.getText().strip(), txtApell.getText().strip()));
            txtCod.setText("");
            txtNom.setText("");
            txtApell.setText("");
            mostrar();
        } else {
            JOptionPane.showMessageDialog(null, "El codigo ya existe.");
            txtCod.requestFocus();
            taResult.setText("No se agrego el proveedor.");
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String valorBuscar = JOptionPane.showInputDialog("Ingrese el codigo de la persona a buscar.");
        buscarProveedor(new Proveedor(valorBuscar));
        if (count == 0) {
            taResult.setText("Cliente no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

