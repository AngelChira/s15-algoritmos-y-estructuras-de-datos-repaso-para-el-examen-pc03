    private void btnEliminarPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPosicionActionPerformed
        int posicion = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la posición a eliminar:"));
        if (posicion < 1) {
            JOptionPane.showMessageDialog(null, "La posición debe ser mayor a 0.");
            return;
        }
        listaCircular.eliminarPosicion(posicion-1);
        mostrar();
    }//GEN-LAST:event_btnEliminarPosicionActionPerformed

    private void btnAgregarPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPosicionActionPerformed
        int posicion = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la posición a eliminar:"));
        if (posicion < 1) {
            JOptionPane.showMessageDialog(null, "La posición debe ser mayor a 0.");
            return;
        }
        String codigo = txtCod.getText().strip();
        if (verificarCod(new Client(codigo))) {
            if (listaCircular.agregarPosicion(new Client(txtCod.getText().strip(), txtNom.getText().strip(), txtApell.getText().strip()), posicion-1)) {
                txtCod.setText("");
                txtNom.setText("");
                txtApell.setText("");
                mostrar();
            }            
        } else {
            JOptionPane.showMessageDialog(null, "El codigo ya existe.");
            txtCod.requestFocus();
            taResult.setText("No se agrego la persona.");
        }
    }//GEN-LAST:event_btnAgregarPosicionActionPerformed

