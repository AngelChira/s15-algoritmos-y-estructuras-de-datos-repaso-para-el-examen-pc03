    public void mostrar() {
        if (listaCircular.tamanio > 0) {
            NodoCircular aux = listaCircular.inicio;
            taResult.setText("CODIGO\t APELLIDOS\t NOMBRES");
            do {
                taResult.append("\n"+aux.getClient().toString());
                aux = aux.getSgte();
            } while (aux != listaCircular.inicio);
        } else {
            taResult.setText("No hay personas.");
        }
    }

    public boolean verificarCod(Client cli) {
        NodoCircular aux = listaCircular.inicio;
        if (aux != null) {
            do {
                if (aux.getClient().getCod().equalsIgnoreCase(cli.getCod())) {
                    return false;
                }
                aux = aux.getSgte();
            } while (aux != listaCircular.inicio);
        }
        return true;
    }
    
    public void buscarPersona(Client cli) {
        count =0;
        NodoCircular aux = listaCircular.inicio;
        if (aux != null) {
            while (aux != null) {
                if (aux.getClient().getCod().equalsIgnoreCase(cli.getCod())) {
                    taResult.setText("PERSONA ENCONTRADA\nCODIGO\t APELLIDOS\t NOMBRES");
                    taResult.append("\n"+aux.getClient().toString());
                    count++;
                }
                aux = aux.getSgte();
            }
        }
    }

